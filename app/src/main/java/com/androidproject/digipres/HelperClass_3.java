package com.androidproject.digipres;

public class HelperClass_3 {
    String cc, rr, adv, drugs;

    public HelperClass_3(String cc, String rr, String adv, String drugs) {
        this.cc = cc;
        this.rr = rr;
        this.adv = adv;
        this.drugs = drugs;
    }

    public String getCc() {
        return cc;
    }

    public void setCc(String cc) {
        this.cc = cc;
    }

    public String getRr() {
        return rr;
    }

    public void setRr(String rr) {
        this.rr = rr;
    }

    public String getAdv() {
        return adv;
    }

    public void setAdv(String adv) {
        this.adv = adv;
    }

    public String getDrugs() {
        return drugs;
    }

    public void setDrugs(String drugs) {
        this.drugs = drugs;
    }
}
