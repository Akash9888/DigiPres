package com.androidproject.digipres;

public class HelperClassTwo {
    String name, phone, age, gender, bp, temp, pulse, weight, height, bmi, cc, rr, adv, drug;

    public HelperClassTwo() {

    }

    public HelperClassTwo(String name, String phone, String age, String gender, String bp, String temp, String pulse, String weight, String height, String bmi, String cc, String rr, String adv, String drug) {
        this.name = name;
        this.phone = phone;
        this.age = age;
        this.gender = gender;
        this.bp = bp;
        this.temp = temp;
        this.pulse = pulse;
        this.weight = weight;
        this.height = height;
        this.bmi = bmi;
        this.cc = cc;
        this.rr = rr;
        this.adv = adv;
        this.drug = drug;
    }
}
